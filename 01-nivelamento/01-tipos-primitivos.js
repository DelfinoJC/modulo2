// Existem seis tipos primitivos em JavaScript:

// number: que representa valores numéricos, incluindo números inteiros e decimais.
// string: que representa valores de texto, como palavras ou frases.
// boolean: que representa os valores verdadeiro (true) ou falso (false).
// undefined: que representa um valor indefinido ou vazio. Uma variável é considerada
// indefinida se não foi atribuído nenhum valor a ela.
// null: que representa a ausência intencional de qualquer objeto ou valor.
// symbol: um tipo novo introduzido no ECMAScript 6 que representa um valor único
// e imutável que pode ser usado como chave para propriedades de objetos.

// Vale ressaltar que em JavaScript, mesmo os objetos sendo considerados um tipo
// não primitivo, eles são muito utilizados e importantes para a linguagem.
